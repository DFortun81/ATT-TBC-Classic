-- Store Spec-Class associations
for specID,classID in pairs(specs) do
	ClassInfoDB.Spec[specID] = classID;
end

-- Store SpecSet-Classes associations
for _,specSetInfo in pairs(specSets) do
	local classID = specs[specSetInfo[1]];
	local specSetID = specSetInfo[2];
	if ClassInfoDB.SpecSet[specSetID] then
		table.insert(ClassInfoDB.SpecSet[specSetID], 1, classID);
	else
		ClassInfoDB.SpecSet[specSetID] = { classID };
	end
end